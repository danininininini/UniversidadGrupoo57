private void cargaAlumnos(){

    for (Alumno item : listaA) {
        alumnoComboBox.addItem(item);
    }

}

    private void armarCabeceraTabla() {
        ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("ID");
        filaCabecera.add("Nombre");
        filaCabecera.add("A√±o");
        for (Object it : filaCabecera) {
            modelo.addColumn(it);
        }
        tablaMaterias.setModel(modelo);
        
    }
    
    private void borrarFilaTabla(){
        int indice =modelo.getRowCount() -1;
        for (int i = indice; i >= 0; i--) {
            modelo.removeRow(i);
        }
    
    }
    
    private void cargaDatosNoInscriptas() {
        borrarFilaTabla();
        Alumno selec = (Alumno) alumnoComboBox.getSelectedItem();
        listaM = inscData.obtenerMateriasNOCursadas(selec.getIdAlumno());
        for (Materia m : listaM) {
            modelo.addRow(new Object[]{m.getIdMateria(), m.getNombre(), m.getAnio()});

        }

    }
    
    private void cargaDatosInscriptas(){
        borrarFilaTabla();
        Alumno selec = (Alumno)alumnoComboBox.getSelectedItem();
        List<Materia> lista= inscData.obtenerMateriasCursadas(selec.getIdAlumno());
        for (Materia m : lista) {
            modelo.addRow(new Object[]{m.getIdMateria(), m.getNombre(), m.getAnio()});
        }
    
    
    }
